document.addEventListener("DOMContentLoaded", () => {

    let selector = document.getElementById('language-selector');

    if (!selector) return;

    // 👇 تحميل اللغة المحفوظة أو الافتراضية
    let savedLang = localStorage.getItem("siteLang") || "en";

    // ✅ هنا الصح
    selector.value = savedLang;
    updateLanguage(savedLang);

    // تغيير اللغة
    selector.addEventListener('change', () => {
        let langValue = selector.value;
        localStorage.setItem("siteLang", langValue);
        updateLanguage(langValue);
    });

    function updateLanguage(language) {

        let nodes = document.querySelectorAll('[data-lang]');

        nodes.forEach(node => {
            let key = node.getAttribute('data-lang');

            if (lang[language] && lang[language][key]) {
                node.textContent = lang[language][key];
            }

            node.classList.toggle('arabic-font', language === 'ar');
            node.classList.toggle('english-font', language === 'en');
        });

        document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr';
        document.body.style.textAlign = language === 'ar' ? 'right' : 'left';

        ['tranding', 'projects', 'testimonial'].forEach(id => {
            let section = document.getElementById(id);
            if (section) {
                section.style.direction = 'ltr';
                section.style.textAlign = 'left';
            }
        });

        let contactSection = document.getElementById('contact');
        if (contactSection) {
            contactSection.style.direction = 'ltr';
            contactSection.style.textAlign = 'left';
        }
    }

});